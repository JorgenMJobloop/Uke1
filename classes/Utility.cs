public class Utility
{
    /// <summary>
    /// A super simple concat method, that add two string together
    /// </summary>
    /// <param name="a">string a</param>
    /// <param name="b">string b</param>
    /// <returns>A new string c</returns>
    public string SimpleConcat(string a, string b)
    {
        return a + " " + b;
    }
}